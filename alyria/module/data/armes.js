/**
 * Retourne un élément aléatoire d'un tableau.
 * @param {Array} arr - Le tableau d'où tirer un élément.
 * @returns {*} Un élément aléatoire du tableau.
 */
function randomFromArray(arr) {
  return arr[Math.floor(Math.random() * arr.length)];
}

/**
 * Retourne un élément aléatoire pondéré d'une liste.
 * @param {Array<Object>} list - Liste d'objets avec une propriété 'weight'.
 * @returns {Object} L'objet choisi de la liste.
 */
function chooseWeighted(list) {
    if (!list || list.length === 0) {
        return null;
    }
    let totalWeight = list.reduce((sum, item) => sum + (item.weight || 1), 0);
    let random = Math.random() * totalWeight;

    for (let i = 0; i < list.length; i++) {
        random -= (list[i].weight || 1);
        if (random <= 0) {
            return list[i];
        }
    }
    return list[list.length - 1];
}

/**
 * Retourne l'article défini approprié ("Le", "La", "L'") pour un genre donné et le mot suivant.
 * Gère l'élision pour les mots commençant par une voyelle ou un 'H' muet.
 * @param {string} gender - Le genre ("m" pour masculin, "f" pour féminin).
 * @param {string} nextWordString - Le mot qui suit immédiatement l'article.
 * @returns {string} L'article approprié.
 */
function getArticleWithElision(gender, nextWordString) {
    if (!nextWordString) return gender === "m" ? "Le" : "La";

    const firstLetter = nextWordString.toLowerCase().charAt(0);
    // Liste non exhaustive des mots avec 'h' aspiré pour éviter l'élision
    const hAspireWords = ["hache", "héro", "handicap", "harpe", "houe", "hussard"];

    if (['a', 'e', 'i', 'o', 'u', 'y'].includes(firstLetter) || (firstLetter === 'h' && !hAspireWords.includes(nextWordString.toLowerCase()))) {
        return "L'";
    } else {
        return gender === "m" ? "Le" : "La";
    }
}

/**
 * Ajuste un mot (adjectif ou adverbe) en fonction du genre et du nombre cibles.
 * @param {Object} wordObj - L'objet mot avec des propriétés comme 'mSingular', 'fSingular', etc.
 * @param {string} targetGender - Le genre cible ("m" ou "f").
 * @param {string} targetNumber - Le nombre cible ("s" pour singulier, "p" pour pluriel).
 * @returns {string} Le mot ajusté pour le genre et le nombre.
 */
function adjustWordForm(wordObj, targetGender, targetNumber = "s") {
    if (!wordObj || !wordObj.type) {
        return "";
    }

    if (wordObj.type === "adverbe") {
        return wordObj.adverb || wordObj.baseWord;
    }

    if (wordObj.type === "adjectif") {
        if (targetGender === "m") {
            return targetNumber === "s" ? wordObj.mSingular : (wordObj.mPlural || wordObj.mSingular);
        } else {
            return targetNumber === "s" ? wordObj.fSingular : (wordObj.fPlural || wordObj.fSingular);
        }
    }
    return wordObj.baseWord;
}

/**
 * Obtient la forme nominale d'un objet mot si disponible.
 * @param {Object} wordObj - L'objet mot.
 * @returns {Object|null} L'objet forme nominale { word: "NomForme", gender: "m/f" } ou null si non trouvé.
 */
function getNounForm(wordObj) {
    if (wordObj && wordObj.nounForm) {
        return wordObj.nounForm;
    }
    return null;
}

